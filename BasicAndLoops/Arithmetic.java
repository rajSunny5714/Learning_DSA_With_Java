package BasicAndLoops;

public class Arithmetic {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y); // for quotient  for int value it show only integer part.
        System.out.println(x%y); // for remainder
    }
}
